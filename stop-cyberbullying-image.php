    <h1 class="text-center text-info m-5">Tips to Help Stop Cyberbullying</h1>
    <p class="display-5 pr-3 text-warning post-time">2016-01-21</p>
    <img src="/wp-content/uploads/2016/01/21.jpg" alt="Tips to Help Stop Cyberbullying" class="aligncenter size-full wp-image-11034" width="182" height="198" /><br />
<br />
With the increasing usage of technology bullying is no longer limited to School or street corners. It can occur anywhere at home, via email, texts, cell phones and social media anytime and there can be hundreds of people involved in it. The person who is a victim of cyberbullying may feel hurt, humiliated, angry or even depressed and suicidal.<br />
<br />
<b>Cyberbullying</b> – <b>Meaning</b><br />
<br />
Cyberbullying happens when a child/teen uses internet, emails, text messages, social media websites, IM’s, online forums to harass or humiliate another child or teen.<br />
<br />
You can follow these tips to protect yourself or your child online and stop problem of cyberbullying:<br />
<br />
<b> </b><br />
<h3>Advice For kids and teens</h3><br />
<b>Understand it is not your fault and stop blaming yourself</b><br />
<br />
It does not matter what the cyberbully says or does, as it is not your fault. You do not have to be ashamed of yourself. The person with the problem is the cyberbully and not you.<br />
<br />
<b>Do not respond / retaliate to Cyberbully</b><br />
<br />
Sometimes the aggressors are looking for a reaction from your end as they feel that it gives them a power over you by doing this. So if you are responding you are empowering the bully. If you retaliate and turn back to the bully you to get involved in the mean act and the chain reaction starts.You must try to remove yourself from the situation and if you can’t you can try humor as it may distract or disarm the bully.<br />
<br />
<b>Save the evidence</b><b> of cyberbullying</b><br />
<br />
You should keep abusive text messages and screenshots of the webpages and report to your parents, teacher or school counselor.You must report the incidents so that the cyberbully does not become aggressive.<br />
<br />
<b>Ask the person to stop bullying</b><br />
<br />
You can ask the person who is bullying you to stop doing that, as you need to make it clear to the person that you will not stand for this treatment. Do not do it if you are not comfortable with this and practice with your parent or friend beforehand.<br />
<br />
<b>Reach o</b><b>ut for help </b><br />
<br />
Talk to your parents, teacher or counselor if the behavior is getting to you. There should be someone who can listen to you and help you through this process.<br />
<br />
<strong>Some instances can be reported to Police</strong><br />
<br />
<strong>If the cyberbully threatens to harm you, sends inappropriate sexual messages, shares your private information/images online you can report that to police and in many cases the cyberbully can be prosecuted by law.</strong><strong></strong><br />
<br />
<b>Do not share your account information with anyone</b><br />
<br />
The passwords of your email accounts or social networking sites should never be shared with anyone including your closest friends.It is also important to keep your phone password protected so no one can impersonate you.<br />
<br />
<b>Take action if someone you know is being bullied</b><br />
<br />
If you see someone getting bullied you can support that person if you can’t stop the aggressor. This is because if you keep standing the aggressor can be empowered. If your friend is being bullied you can listen to him/her and see how you can help. You may together decide how to report the bullying.<br />
<br />
&nbsp;<br />
<h3>Additional advice for parents</h3><br />
<b>Help your Child if he/she reports Cyberbullying incident</b><br />
<br />
Most of the kids do not tell their parents about online or offline bulling this is because they fear that they may lose their computer or cell phone privileges by doing so. If you observe your child not sleeping properly or does not want to go to school or find him agitated when he/ she is on computer or phone ask them calmly the reason for it. Ask them if they have any issues with mean behavior. Listen to what they say carefully and if they report any cyberbullying incident help them.<br />
<br />
<b>Save the threats</b><br />
<br />
Do not reply to any incidents of cyberbullying, instead save the harassing messages, sexually explicit pictures and other text to report to police after seeking appropriate legal advice.<br />
<br />
<b>Report incident to ISP/ Social networking sites</b><br />
<br />
You can report such incidents to ISP and the social website that was used. The profile of the person who is involved in cyberbullying can be reported and blocked. The cyberbully’s email address and cell phone number can be blocked and you can also change your child’s email address or phone number for their safety and security.<br />
<br />
<b>Report the cyberbully’s parents or school if you recognize him</b><br />
<br />
If you identify the cyberbully you can talk to his/ her parents and inform them regarding the incident. You can also report of this incident at your child’s school if the cyberbully is also studying there. Check with your child if he/she wants to handle the matter privately before reporting the issue at school.<br />
<br />
<b>More than one perspective needed</b><br />
<br />
It is very important to understand the complete truth from everyone’s perspective before responding to any incident. You have to be open- minded and identify if there were any chain reactions involved where your kid too responded to the bully in a similar manner.<br />
<br />
<b>Listen carefully to your kid if they have been bullied</b><br />
<br />
It is vital that you hear and listen to your kid carefully when they come for your help. You must involve them and respond to them thoughtfully.The child heals up from such incidents sooner if he/she is heard respectfully.<br />
<br />
<b>Help your child restore self-respect </b><br />
<br />
You may need to help restore the self- respect and resilience in your kid. Your child may have lost dignity or confidence due to any such incident and you may need to focus on helping your kid heal. You can decide along with your child how you will deal with the cyberbully.<br />
<br />
<b>Monitor your child’s technology use</b><br />
<br />
In order to prevent cyberbullying and protect your child you can monitor what they do online by keeping the computer in the busy area. You can also limit the data access to your child’s smartphone. You can also set up filters in your child’s computer and install the tracking software to block inappropriate content and help you check the online activities of your kid. Also ask your child to discuss with you if they receive any threatening message online and reassure them that they will not lose cell phone or computer privileges even if any such issue occurs.<br />
<br />
